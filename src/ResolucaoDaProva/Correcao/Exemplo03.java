package ResolucaoDaProva.Correcao;

public class Exemplo03 {
    public static void main(String[] args) {

        int counter = 0;  //Adição do tipo inteiro a variável counter
        //Trocando para zero, o número dois será incluído.
        do {
            counter += 2;
            System.out.println(counter);
        } while (counter < 100);//Em java while só pode ser digitado com letras minúsculas.

    }
}
